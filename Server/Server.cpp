					{
						SOCKET outSock = master.fd_array[i];
						if (outSock != ListenSocket && outSock != sock)
						{
							std::string ss = "Socket :";
							ss += " :";
							ss += sock;
							ss += " :";
							ss += recvbuf;
							ss += "\n";
							send(outSock, ss.c_str(), ss.size() + 1, 0);
						}
					}
